package day16_nestedloop;

public class loopPractices {

    public static void main(String[] args) {

        for(int i = 1; i < 6; i++) {

            System.out.println("Hello" + i);

        }

        System.out.println("=====================================================");

        int b = 0;

        while( b < 5){

            System.out.println("Hello" +b);
            b ++;
        }
        System.out.println("+=+++++++++++++++++++++++++++++++++++++++++++++++++++++++++");

        int k = 0;

        do{

            System.out.println("Hello" +k);
            k++;

        }while(k < 5);









    }


}
